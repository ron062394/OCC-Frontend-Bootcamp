//Logical expressions
let sunny = false;
let warm = false;

/*
AND
true && true = true
false && true = false
true && false = false
false && false = false
*/
console.log("Is it sunny AND warm?", sunny && warm);
console.log("Is it sunny OR warm", sunny || warm);
/*
OR
true || true = true
false || true = true
true || false = true
false || false = false
*/